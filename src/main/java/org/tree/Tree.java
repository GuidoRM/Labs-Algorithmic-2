package org.tree;

public class Tree {
    private long value;
    private Tree left;
    private Tree right;

    public Tree(long value) {
        this.value = value;
    }

    public long getValue() {
        return value;
    }

    public void setValue(long value) {
        this.value = value;
    }

    public Tree getLeft() {
        return left;
    }

    public void setLeft(Tree left) {
        this.left = left;
    }

    public Tree getRight() {
        return right;
    }

    public void setRight(Tree right) {
        this.right = right;
    }

    public boolean isLeaf() {
        return left == null && right == null;
    }
}
